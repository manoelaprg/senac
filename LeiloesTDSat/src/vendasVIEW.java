    public void mostrarProdutosVendidos() {
        List<ProdutosDTO> produtosVendidos = produtosDAO.produtosVendidos();

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Nome");
        model.addColumn("Valor");
        model.addColumn("Status");

        for (ProdutosDTO p : produtosVendidos) {
            model.addRow(new Object[]{p.getId(), p.getNome(), p.getValor(), p.getStatus()});
        }
        
        listaProdutos.setModel(model);                             

    }

